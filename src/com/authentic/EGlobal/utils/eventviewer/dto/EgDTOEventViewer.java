package com.authentic.EGlobal.utils.eventviewer.dto;

public class EgDTOEventViewer {
  private int id_evento;
  
  private String codigo_evento;
  
  private String descripcion_evento;
  
  private String ips_nombre;
  
  private int cantidad_evento;
  

public int getId_evento() {
	return id_evento;
}



public void setId_evento(int id_evento) {
	this.id_evento = id_evento;
}



public String getCodigo_evento() {
	return codigo_evento;
}



public void setCodigo_evento(String codigo_evento) {
	this.codigo_evento = codigo_evento;
}



public String getDescripcion_evento() {
	return descripcion_evento;
}



public void setDescripcion_evento(String descripcion_evento) {
	this.descripcion_evento = descripcion_evento;
}



public String getIps_nombre() {
	return ips_nombre;
}



public void setIps_nombre(String ips_nombre) {
	this.ips_nombre = ips_nombre;
}



public int getCantidad_evento() {
	return cantidad_evento;
}



public void setCantidad_evento(int cantidad_evento) {
	this.cantidad_evento = cantidad_evento;
}

  
  
}